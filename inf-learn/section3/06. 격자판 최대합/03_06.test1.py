def solution(N, inp):
    answer = 0
    maxVal = 0
    # 행 계산 먼저
    for i in range(N):
        tmp_sum = 0
        for j in range(N):
            tmp_sum += inp[i][j]
        if maxVal < tmp_sum :
            maxVal = tmp_sum
        print('t', tmp_sum)
    print(maxVal)

    # 다음으로 열 계산
    for i in range(N):
        tmp_sum = 0
        for j in range(N):
            tmp_sum += inp[j][i]  # 여기만 변경
        if maxVal < tmp_sum :
            maxVal = tmp_sum
        print('t', tmp_sum)
    print(maxVal)

    # 대각선 계산
    tmp_sum = 0
    for i in range(N):
        #tmp_sum = 0  # 여기 있으면 안돼!
        for j in range(N):
            if i == j :
                tmp_sum += inp[i][j]
            else:
                continue
        if maxVal < tmp_sum :
            maxVal = tmp_sum
        print('t', tmp_sum)
    print(maxVal)

    # 역 대각선 계산
    tmp_sum = 0
    for i in range(N):
        #tmp_sum = 0  # 여기 있으면 안돼!
        tmp_sum += inp[i][N-1-i]

        if maxVal < tmp_sum :
            maxVal = tmp_sum
    print(maxVal)

    return answer

