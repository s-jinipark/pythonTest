def solution(N, inp):
    answer = 0
    # 자신의 상하좌우 숫자보다 큰 숫자
    dx = [-1, 1, 0, 0] # 상하
    dy = [0, 0, -1, 1] # 좌우  #=> 여기서는 x/y 가 행/열

    # 테두리를 친다
    #연습 = [[0]*(N+2)] *(N+2)  # OMG 이렇게 하면 안됨!!
    tmp = [[0 for _ in range(N+2)] for _ in range(N+2)]
    print(tmp)
    for i in range(N):
        for j in range(N):
            #print(inp[i][j], i, j, i+1, j+1)
            tmp[i+1][j+1] = inp[i][j]

    #print(연습)
    beauti_prt(tmp)
    for i in range(1, N+1):
        for j in range(1, N+1):
            #print(i, j)
            bon = tmp[i][j]
            # 상하좌우 비교
            for k in range(4):
                comp = tmp[i+dx[k]][j+dy[k]]
                #print(bon, comp)
                if bon <= comp :
                    break
            else:   # 여기 point
                answer +=1
                print(bon, ':', i, j)

    return answer

