def solution(N, M, inp):
    answer = 0

    for i in range(N):
        print(inp[i])
        tmp_sum = inp[i]
        if tmp_sum == M:
            answer += 1
            print(i, tmp_sum)
            continue
        for j in range(i+1, N):
            tmp_sum += inp[j]
            print('>', inp[j])
            if tmp_sum == M :
                answer += 1
                print('i=', i, 'j=', j, 'tmp_sum=', tmp_sum)
                break
            elif tmp_sum > M :
                break

    return answer

