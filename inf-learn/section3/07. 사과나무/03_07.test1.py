def solution(N, inp):
    answer = 0

    # N은 항상 홀수 이니, 중간 값을 get하고
    # 루핑 돌면서 중간값보다 작은 경우, 같은 경우, 큰 경우 나누어서
    mid = N//2  # + 1
    #print(mid)
    for i in range(N):
        tmp = mid-i
        if tmp < 0 :
            tmp *= -1
        #print(i, tmp, N-tmp)
        for j in range(tmp, N-tmp):
            #print('>', j)
            answer += inp[i][j]

    return answer

N = 5
